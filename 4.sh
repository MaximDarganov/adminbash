#!/bin/bash

# Проверка аргумента
if [ $# -ne 1 ]; then
    echo "Использование: $0 <название_планеты>"
    exit 1
fi

# Ассоциативный массив с данными о планетах
declare -A planets=(
    ["Меркурий"]=0
    ["Венера"]=0
    ["Земля"]=1
    ["Марс"]=2
    ["Юпитер"]=79
    ["Сатурн"]=82
    ["Уран"]=27
    ["Нептун"]=14
)

planet=$1

# Проверка существования планеты в базе данных
if [[ -n "${planets[$planet]}" ]]; then
    echo "${planets[$planet]}"
else
    echo "Ошибка: Планета '$planet' не найдена в базе данных"
    exit 1
fi 